void main() {
  //Identify the data type of each of the following values in Dart: - 3.14 - "Hello" - true -42
  int myInteger = 42;
  double myDouble = 3.14;
  bool myBoolean = true;
  String myString = "Hello";
  print('Value: $myDouble, Type: ${myDouble.runtimeType}');
  print('Value: $myString, Type: ${myString.runtimeType}');
  print('Value: $myBoolean, Type: ${myBoolean.runtimeType}');
  print('Value: $myInteger, Type: ${myInteger.runtimeType}');
}
